export default (state = INIT_STATE, action) => {
  switch (action.type) {
    case 'DELETE_CART':
      const i = action.payload;
      const cost = state.cart[i]['cost'];
      state.totalCost -= cost;
      state.cart = state.cart
        .slice(0, i)
        .concat(state.cart.slice(i + 1, state.cart.length));
      return {...state};

    case 'ADD_TO_CART':
      state.cart.push(action.payload);
      state.totalCost += action.payload['cost'];
      return {
        ...state,
      };
    case 'ADD_TO_FAVORITE':
      const pType = action.payload['type'];
      const pId = action.payload['id'];
      for (let j = 0; j < state.digitalTools.length; j++) {
        for (let k = 0; k < state.digitalTools[j].length; k++) {
          if (
            pType === state.digitalTools[j][k]['type'] &&
            pId === state.digitalTools[j][k]['id']
          ) {
            state.digitalTools[j][k]['favorite'] = true;
          }
        }
      }
      state.favorite.push(action.payload);
      return {
        ...state,
      };
    case 'DELETE_FROM_FAVORITE':
      const type = action.payload['type'];
      const id = action.payload['id'];
      for (let j = 0; j < state.digitalTools.length; j++) {
        for (let k = 0; k < state.digitalTools[j].length; k++) {
          if (
            type === state.digitalTools[j][k]['type'] &&
            id === state.digitalTools[j][k]['id']
          ) {
            state.digitalTools[j][k]['favorite'] = false;
          }
        }
      }
      for (let k = 0; k < state.favorite.length; k++) {
        if (
          type === state.favorite[k]['type'] &&
          id === state.favorite[k]['id']
        ) {
          state.favorite = state.favorite
            .slice(0, k)
            .concat(state.favorite.slice(k + 1, state.favorite.length));
        }
      }
      return {...state};
    default:
      return state;
  }
};
